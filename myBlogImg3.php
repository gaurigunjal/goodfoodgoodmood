	<pre>Ingredients of Spring Rolls

    For the pancakes:
    1/2 cup flour
    1 egg
    1/4 tsp salt
    1/4 cup water
    1/4 cup milk
    3 Tbsp oil mix the water, oil and milk together
    For the filling:
    1 cup thinly shredded cabbage
    1 cups spring onions-including leaves, finely chopped
    1 cup thinly shredded carrot
    1/2 tsp salt
    2 Tbsp oil
    4 cloves garlic-very finely chopped
    1 tsp soya sauce
    2 Tbsp celery-chopped fine
    1 Tbsp flour, made into thin paste by adding water
    Oil for deep frying
    </pre>
	</div>


	<div class="col-md-8">
		<pre>
	How to Make Spring Rolls

    1.
    Make the pancakes with the pancake ingredients.
    2.
    Heat the 1 tbsp oil and stir-fry the garlic and 
    onions over high heat till the onions are a little soft.
    3.
    Add the rest of the filling ingredients and turn around 
    a few times, still over a high heat.
    4.
    Take a pancake and place a heaped tbsp of filling towards one edge.
    5.
    Fold that edge over the filling, then the two sides thus 
    formed over the fold, and roll over till the end, sealing 
    that edge with the flour paste.
    6.
    It is very important to seal the filling in, so that 
    it does not spill out while frying. Double fry the pancakes.
		</pre>
