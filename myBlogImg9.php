	How to Make Mocha Dacquoise Cake

Preheat the oven to 250°. Make the meringue: In a stand mixer 
on high speed, beat the egg whites until very stiff peaks form, 
2-3 minutes.
Meanwhile, in a food processor, pulse the almond flour, sugar, 
and cornstarch until the mixture reaches a fine, sandy consistency. 
Transfer to a large bowl.
Trace out three 10 inch circles on parchment paper as a guide. 
Set them on 1-2 large baking sheets lined with silicone mats.
Using a rubber spatula and working in three batches, fold the 
egg whites into the almond mixture until thoroughly combined. 
Scoop the mixture evenly onto the traced circles and, using an 
offset spatula, spread the meringue evenly to fill the circles 
(they will be about 1⁄4 inch high each).
Bake, rotating the pans fully every 10 minutes, until an even 
golden brown is achieved, about 2 hours and 20 minutes (though 
time will vary with consistency and humidity).
Remove and let cool slightly. The shells will have a tendency to 
stick; work slowly and carefully to remove parchment from them 
(the rounds will be very fragile).
Make the buttercream: In a high-sided saucepan over medium heat, 
add the coffee and sugar. Set a candy or deep-fat thermometer 
into the pan and heat until the mixture registers 265°.
