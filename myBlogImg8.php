		<pre>
	How to Make Pain au Chocolat

 Stir together yeast and ½ cup water heated to 115° together 
 in the bowl of a stand mixer fitted with a dough hook; let 
 sit until foamy, about 10 minutes. Stir in milk, sugar, 3 tbsp. 
 melted butter, powdered milk, and salt; add flour. Mix on medium 
 speed until a stiff dough forms, about 5 minutes. Transfer to a 
 work surface and form into a thick square; wrap in plastic wrap 
 and refrigerate for 2 hours.
Place remaining butter on a sheet of plastic wrap; cover with 
another sheet. Using a rolling pin, pound and shape butter into 
a 6″, ½″-thick square; set aside. Using rolling pin, roll dough
 into an 16″ square on a lightly floured work surface. Unwrap butter 
 square, and place on dough so its corners line up with the middle 
 point of each side of dough square; fold dough corners over butter 
 so they meet in the center. Roll dough into a 12″ x 9″ rectangle, 
 and then tri-fold dough like a letter. Roll the dough into a 12″ x 9″ 
 rectangle and repeat folding. Wrap in plastic wrap; chill for 30 minutes. 
 repeateat rolling and folding dough twice more; chill for 1 hour.
